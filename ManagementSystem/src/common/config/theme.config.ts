export type ThemeConfig = typeof themeConfig

/**
 * Get a theme value by path
 * @example getThemeValue('colors.primary.500') // Returns '#0ea5e9'
 */
export function getThemeValue(path: string): any {
  return path.split('.').reduce((obj, key) => obj?.[key], themeConfig as any)
}
